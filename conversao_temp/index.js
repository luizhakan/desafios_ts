var temperaturaCelsius = 30;
var temperaturaFarenheit = 86;
var celsiusParaFarenheit = (temperaturaCelsius * 9) / 5 + 32;
var farenheitParaCelsius = ((temperaturaFarenheit - 32) * 5) / 9;
console.log("30\u00BA Celsius equivalente a ".concat(celsiusParaFarenheit, "\u00BA farenheit"));
console.log("86\u00BA Farenheit equivalente a ".concat(farenheitParaCelsius, "\u00BA celsius"));
